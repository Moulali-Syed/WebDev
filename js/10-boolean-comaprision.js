//boolean and comparision operator

//boolean - true , false
let num1 = 5;
let num2 = '5';

console.log(num1 > num2);

//== vs ===
let a = 10;
let b = '10';
console.log(a == b); //checks only value
console.log(a === b); //checks value and datatype

// != vs !==
console.log(num1 != num2); // checks only value
console.log(num1 !== num2); //checks value and type
